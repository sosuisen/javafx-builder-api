
package io.github.sosuisen.jfxbuilder.controls;

/**
 * The {@code TextFieldBuilder} class constructs instances of the {@link javafx.scene.control.TextField TextField} class 
 * and offers a fluent interface for creating and configuring it. 
 *
 * <p>
 * This class includes a static {@code create} method that accepts the same arguments as the original {@link javafx.scene.control.TextField TextField} constructor
 * and returns an instance of the {@code TextFieldBuilder}.
 * </p>
 *
 * <p>
 * You can use method chaining to call the builder methods for configuring the {@link javafx.scene.control.TextField TextField}.
 * Finally, invoke the {@code build} method to generate an instance of the {@link javafx.scene.control.TextField TextField} class.
 * </p>
 *
 * <p>
 * Note that intermediate builder methods are not evaluated until the {@code build} method
 * is called, meaning they are evaluated lazily.
 * </p>
 *
 * @author Hidekazu Kubota &lt;hidekazu.kubota@gmail.com&gt;
 */

